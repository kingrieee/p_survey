								<h2><strong>ACCIDENT </strong></h2>
								<p> -  to dream that you are in an accident signifies pent-up guilt in which you are subconsciously punishing yourself over. Perhaps you are not proud of something you have done. Alternatively, the accident may symbolize an error or mistake you have made. Accident dreams also represent your fears of being in an actual, physical accident. You may simply be nervous about getting behind the wheel. Or the dream is trying to warn you of some accident. 
								</p>
								<p> - to dream of a car accident symbolizes your emotional state. You may be harboring deep anxieties and fears. Are you "driving" yourself too hard? Perhaps you need to slow down before you hit disaster. You need to rethink or re-plan your course of actions and set yourself on a better path. Dreaming that someone is injured in a car accident means that you cannot control the action of others. They have to live with the consequences of their own decisions.
								</p>
								<p> - to dream that a loved one dies in an accident indicates that something within your own Self is no longer functional and is "dead". It is also symbolic of your own relationship with that person. Perhaps you need to let go of this relationship. If you dream that you die from the car accident and actually see the reactions of your loved ones, then it suggests that your reckless activity is affecting those around you. This dream is a wake-up call. 
								</p>	
